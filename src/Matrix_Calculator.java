       public int [] operate(int[] matrix1,int[] matrix2,char c){
       
       int i;int l=matrix1.length; 
       int [] result=new int[l];
       
       for (i=0;i<l;i++){
       
           if (c=='+'){
           result[i] =matrix1[i]+matrix2[i];
           }else 
           if (c=='-'){
           result[i] =matrix1[i]-matrix2[i];
           } 
       }// end of for loop  
       return result;
     }
