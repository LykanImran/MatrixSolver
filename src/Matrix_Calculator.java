      public String[] getMatrix(String string){
        string=string.trim();       
        int enterIndex=string.indexOf("\n");          // index of enter 
        String substring=string.substring(0,enterIndex);//EXTRACTS FIRST LINE OF MATRIX
        substring=substring.replaceAll("\\s{2,}", " ");// replaces 2 or more consecutive spaces
        substring=substring.trim();
        
        int column=substring.replaceAll("[^ ]", "").length();// replaces all characters except spaces and counting
        column++;
        
        string=string.replaceAll("\\s{2,}", " ");
        string=string.replaceAll("\n", " ");/// replacing enter into spaces ... so as to count no. of exact spaces
        String dummy=string;// making a dummy string to use it next function
      
        int total=dummy.replaceAll("[^ ]", "").length();// repalcing al characters of whole strings
        total++;
        
        String totalInString=String.valueOf(total);
        String columnInString=String.valueOf(column); 
            
        String[] data={string,columnInString,totalInString}; /// Returning Data  from  this point    
        return data;   
    }
