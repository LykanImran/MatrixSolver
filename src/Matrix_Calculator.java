       public int[] structureMatrix(String matrix,int total1){
         int i;int[] matrixArray=new int[total1];
         int key=0;     
         // extracting each word as a token with StringTokenizer class
         StringTokenizer t=new StringTokenizer(matrix);
         String word="";
         while(t.hasMoreTokens()){
         word=t.nextToken();
         matrixArray[key]=Integer.parseInt(word);  
         key++;
         }
         return matrixArray;  
       }
